public class Kalkulacka {
    double cislo1;
    double cislo2;

    public double soucet() {
        return cislo1 + cislo2;
    }

    public double rozdil() {
        return cislo1 - cislo2;
    }

    public double soucin() {
        return cislo1 * cislo2;
    }

    public double podil() {
        return cislo1 / cislo2;
    }
}
